from datetime import datetime
from datetime import date

print(datetime.now())  # current date time
print(date.today())  # current date
print(datetime.now().date())  # current date
print(datetime.now().time())  # current time
print(datetime.now().year)  # current year
print(datetime.now().month)  # current month
print(datetime.now().day)  # current day of month
